    //Boton agregar
    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        if(vendedor.getText().isEmpty() | sencillo.getText().isEmpty() | doble.getText().isEmpty()|
                bonisote.getText().isEmpty() | mega.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "No pueden haber campos vacios");
        return;
        }
        int posicion = this.posicionVacia();
        if(posicion != -1){
            try{
                nombres[posicion] = vendedor.getText();
                helados[posicion][0] = Integer.parseInt(sencillo.getText());
                helados[posicion][1] = Integer.parseInt(doble.getText());
                helados[posicion][2] = Integer.parseInt(bonisote.getText());
                helados[posicion][3] = Integer.parseInt(mega.getText());
                defaultTable1.setRowCount(0);
                defaultTable2.setRowCount(0);
                this.imprimirArreglos();
                this.limpiarCampos();
                if(posicion+1==nombres.length){
                    agregar.setEnabled(false);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(rootPane, "Error al agregar");
                nombres[posicion] = null;
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Limite alcanzado");
        }
        
    }//GEN-LAST:event_agregarActionPerformed
    //Boton reiniciar
    private void reiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reiniciarActionPerformed
        defaultTable1.setRowCount(0);
        defaultTable2.setRowCount(0);
        totalVendedores.setText("");
        totalVendedores.setEnabled(true);
        totalVendedores.requestFocus();
        vendedor.setText("");
        sencillo.setText("");
        bonisote.setText("");
        mega.setText("");
        doble.setText("");
        nombres=null;
        this.desactivarElementos();
    }//GEN-LAST:event_reiniciarActionPerformed
    
