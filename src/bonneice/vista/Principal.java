    //Funcion que verifica las teclas presionadas y manda un mensaje si no es numero
    public void verificar(KeyEvent evt){
    char caracter = evt.getKeyChar();
        if(Character.isLetter(caracter)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Igrese solo numeros");
        }
    }
    
    //Manda la primera posicion vacía que encuentre
    public int posicionVacia(){
        int posicion = -1;
        for(int i=0; i<nombres.length; i++){
            if(nombres[i]==null){
                posicion = i;
                break;
            }
        }
        return posicion;
    }
    
    //Imprime en las dos tablas
    public void imprimirArreglos(){
        int[] contador = new int[4];
        for(int fila=0; fila<nombres.length; fila++){
            
            if(nombres[fila]!=null){
                Object[] objeto = new Object[5];
                objeto[0] = nombres[fila];
                
                for(int columna=0; columna<helados[fila].length; columna++){
                    objeto[columna+1] = helados[fila][columna];
                    contador[columna] = contador[columna] + helados[fila][columna];
                }
                defaultTable1.addRow(objeto);
            }
        }
        Object[] objeto2 = {contador[0], contador[1], contador[2], contador[3]};
        defaultTable2.addRow(objeto2);
    }
    
    
