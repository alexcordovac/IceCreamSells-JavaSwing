    //Insertar el total de vendedor y al presionar enter guardar
    private void totalVendedoresKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_totalVendedoresKeyPressed
        try{
            if(evt.getKeyCode()== KeyEvent.VK_ENTER){
                int n = Integer.parseInt(totalVendedores.getText());
                nombres = new String[n];
                helados = new int[n][4];  
                totalVendedores.setEnabled(false);
                this.activarElementos();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Numero no valido");
            totalVendedores.setText("");
        }
    }//GEN-LAST:event_totalVendedoresKeyPressed

